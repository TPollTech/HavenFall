export function drawSprite(ctx, sheetImages, sheet, name, dx, dy, scale = 1) {
  const s = SPRITES[sheet]?.[name];
  if (!s) { console.warn(`Sprite não encontrado: ${sheet}/${name}`); return; }
  const img = sheetImages[sheet];
  ctx.drawImage(img, s.x, s.y, s.w, s.h, dx, dy, Math.round(s.w * scale), Math.round(s.h * scale));
}

export function getSpriteSize(sheet, name, scale = 1) {
  const s = SPRITES[sheet]?.[name];
  if (!s) return null;
  return { w: Math.round(s.w * scale), h: Math.round(s.h * scale) };
}
